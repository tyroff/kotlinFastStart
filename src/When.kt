fun main() {
    val nameOfMonth = "Марк"
    val season: String = when(nameOfMonth) {
        "Декабрь", "Январь", "Февраль" -> {
            "Зима"
        }
        "Март", "Апрель", "Май" -> {
            "Весна"
        }
        "Июнь", "Июль", "Август" -> {
            "Лето"
        }
        "Сентябрь", "Октябрь", "Ноябрь" -> {
            "Осень"
        }
        else -> {
            "Нет такого месяца :("
        }
    }
    println(season)
}